package avaliacao1;

public class Carro {
	
	private String nome;
	private double valor;
	
	
	//setters e getters
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public double getValor() {
		return valor;
	}
	public void setValor(double valor) {
		this.valor = valor;
	}		

}
